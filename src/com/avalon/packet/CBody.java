package com.avalon.packet;

public class CBody {
	private String dataType; // UserEntry / RoomEntry / MessageEntry
	private Object message = new Object();
	public CBody() {
		this.dataType = "";
		this.message = "";
	}
	public CBody(String type, Object message){
		this.dataType = type;
		this.message = message;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public Object getMessage() {
		return message;
	}
	public void setMessage(Object message) {
		this.message = message;
	}
	
	
	
}
